import type { AnimalType } from '../../types'
import styles from './PixelAnimal.module.css'

interface PixelAnimalProps {
  animal: AnimalType
  size?: number
}

export function PixelAnimal({ animal, size = 120 }: PixelAnimalProps) {
  const scale = size / 16
  return (
    <div
      className={`${styles.container} ${styles[animal]}`}
      style={{ width: size, height: size }}
    >
      <svg
        viewBox="0 0 16 16"
        width={size}
        height={size}
        style={{ imageRendering: 'pixelated' }}
      >
        {PIXELS[animal].map((row, y) =>
          row.map((color, x) =>
            color ? (
              <rect
                key={`${x}-${y}`}
                x={x}
                y={y}
                width={1}
                height={1}
                fill={color}
                className={getPixelClass(animal, x, y)}
              />
            ) : null,
          ),
        )}
      </svg>
      <div className={styles.shadow} style={{ width: scale * 10, height: scale * 2 }} />
    </div>
  )
}

function getPixelClass(animal: AnimalType, x: number, y: number): string | undefined {
  switch (animal) {
    case 'ox':
      // 꼬리 (오른쪽 상단)
      if (x >= 13 && y <= 6) return styles.oxTail
      // 귀
      if ((x === 4 || x === 11) && y <= 2) return styles.oxEar
      return undefined
    case 'meerkat':
      // 눈
      if (y === 5 && (x === 6 || x === 9)) return styles.meerkatEye
      // 머리 전체 (두리번)
      if (y <= 7) return styles.meerkatHead
      return undefined
    case 'quokka':
      return styles.quokkaBounce
    case 'hedgehog':
      // 가시 (상단)
      if (y <= 4) return styles.hedgehogSpine
      return undefined
    case 'bird':
      // 날개
      if ((x <= 3 || x >= 12) && y >= 5 && y <= 9) return styles.birdWing
      return styles.birdFloat
  }
}

